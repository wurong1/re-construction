function format(obj) {
  let options = new Array();
  for( let name in obj) {
    if(!!name) {
      const len = obj[name].length;
      if(len === 1) {
        options.push({'value': name, 'label': name});
      }else {
        let child = new Array();
        for(let i = 0; i < obj[name].length; i++) {
          child.push({'value': obj[name][i].Name, 'label': obj[name][i].Name});
        }
        options.push({'value': name, 'label': name, 'children': child});
      }
    }
  }
  return options;
}

